public class Reverse_Array {

	 public static void main (String args []) {
		 
		  int arr [] = { 4,5,8,9,7};
		  
		    System.out.println("Reverse Array is :");
		    
		     for (int i =  arr.length-1; i>= 0; i--) {
		    	 
		    	  System.out.print(arr[i] + " ");
		     }
	 }
}
